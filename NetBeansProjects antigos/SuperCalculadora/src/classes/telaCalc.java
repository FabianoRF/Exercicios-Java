    private void btncalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncalcularActionPerformed
        pancalc.setVisible(true);
        
        int n;
        n = Integer.parseInt(barranumero.getValue().toString());
        
        int mod= n%2;
        lblmod.setText(Integer.toString(mod));
        double cubo= Math.pow(n, 2);
        lblcubo.setText(Double.toString(cubo));
        double sqrt= Math.sqrt(n);
        lblraiz.setText(String.format("%.2f", sqrt));
        double cbrt= Math.cbrt(n);
        lblraizcubica.setText(String.format("%.2f", cbrt));
        lblabsoluto.setText(Integer.toString(Math.abs(n)));
        
    }//GEN-LAST:event_btncalcularActionPerformed

